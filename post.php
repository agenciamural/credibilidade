                        <p><strong>Guarulhos</strong></p>
                        <p>Os candidatos a prefeito Guti (PSB) e Eli Corrêa Filho (DEM) são os nomes que disputarão o segundo turno na cidade de Guarulhos, contrariando a maioria das pesquisas, que apontavam Elói Pietiá (PT), junto com Eli Corrêa Filho no segundo turno.</p>
                        <p>Guti liderou toda a votação neste domingo, alcançando a diferença de 73.457 de votos, com 34,54 % de votos válidos totalizando 208.591 para Guti e 135.134 para Eli. Ambos nunca foram eleitos na cidade.</p>
                        <p>O terceiro lugar ficou com Elói Pieta (PT) com 116.676, eleito duas vezes Prefeito na Cidade com o término do mandato em 2008. Xerife (PRB) levou a 4a posição com 65.796 dos votos.</p>
                        <p>O número de votos nulos na cidade foi 94.790 (12,70%), enquanto os votos em branco ultrapassaram 5%, totalizando 47.843 votos. O segundo turno acontece daqui a três semanas, no dia 30 de outubro.</p>
                        <p><strong>Osasco</strong></p>
                        <p>Os osasquenses também terão que esperar mais um pouco para conhecer quem assume o cargo de prefeito na cidade. Com 39,44% dos votos, Rogério Lins (PTN) enfrenta no segundo turno o atual prefeito, Jorge Lapas (PDT), que teve 38,55% dos votos. Como o Mural já havia adiantado, <a href="http://www1.folha.uol.com.br/poder/eleicoes-2016/2016/09/1812341-candidato-do-psdb-e-barrado-e-osasco-pode-ter-segunda-eleicao-decidida-na-justica.shtml" target="_blank" rel="noopener">o candidato Celso Giglio (PSDB) teve 71.657 votos invalidados</a> devido a inelegibilidade da candidatura.</p>
                        <p><strong>São Bernardo do Campo</strong></p>
