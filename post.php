                        <p><strong>Embu da Artes</strong></p>
                        <p>Ney Santos (PRB) é o novo prefeito de Embu das Artes. Com 79,45% dos votos, o candidato venceu a disputa contra os outros seis concorrentes.</p>
                        <p>Atualmente Ney ocupa o cargo de Presidente da Câmara Municipal, que assumiu em 2012 após ser eleito com mais de oito mil votos para vereador. Sua candidatura foi polêmica e envolveu julgamento por compra de votos, em 2012, e pedido de afastamento do cargo, do qual recorreu e se manteve no posto. Somente na reta final para as eleições, em 23 de setembro, ele foi autorizado pela justiça a seguir oficialmente com sua candidatura.</p>
                        <p>O segundo mais votado, com mais de 35 mil votos foi o Geraldo Cruz (PT), mas a porcentagem não foi computada porque ele está inelegível por uso indevido de publicações em jornal, em 2014.</p>
                        <p>A distribuição de votos para prefeito ficou da seguinte maneira: Dr. Almir (PEN) 2,44%; Juninho (PSOL) 7,12%; Nivaldo Orlandi (PDT) 1,26%; Dr. Pedro Valdir (PSD) 9,45% e Toninho Serapião (PMN) 0,29%.</p>
                        <p>A cidade tem cerca de 168 mil eleitores e aproximadamente 90% compareceram às urnas. Várias ruas amanheceram inundadas por santinhos e, no momento da votação, a reportagem do Mural foi abordada por “bocas de urna”. A atividade, ilegal, rendeu para o município cinco prisões, de acordo com o boletim do TRE/SP.</p>
                        <p><strong>Embu-Guaçu</strong></p>
                        <p>Pela primeira vez em 51 anos de emancipação política, Embu-Guaçu será governada por uma mulher.</p>
                        <p>A candidata Maria Lúcia (PSB) se elegeu com 35,69% dos votos, totalizando 12.494. Ela, juntamente com Marcia Almeida, que se elegeu vereadora pelo PSD, com 607 votos, representarão as mulheres na política da cidade.</p>
                        <p>Em segundo lugar ficou Zé Antonio (PTB), com 26,86% dos votos. Alexandre Rainha (PMDB), apoiado pelo atual prefeito Clodoaldo Leite (PMDB), somou apenas 8.550, ocupando o terceiro lugar com 24,42% e André Ferreira (PSC), com 12% (4.202), ficou em quarto. Pelo PSTU, Paulinho Corretor e Nena Conceição (PTC) tiveram menos que 1% de eleitores.</p>
                        <p><strong>Ferraz de Vasconcelos</strong></p>
                        <p>O ex-prefeito de Ferraz de Vasconcelos José Carlos Chacon, o Zé Biruta (PRB) foi eleito com 35,95% dos votos, vencendo a ex-primeira dama Elaine Abissamra (PSB) e o atual prefeito José Izidoro Neto (PMDB), que tentava a reeleição.</p>
                        <p><strong>Itaquaquecetuba</strong></p>
                        <p>Em Itaquaquecetuba, Mamoru Nakashima (PSDB) foi reeleito prefeito com mais de 63% dos votos válidos, derrotando outros 12 candidatos que disputavam a vaga. É o primeiro prefeito tucano eleito na cidade, que tem um histórico de rejeição ao partido. Em 2012, Nakashima foi eleito pelo PTN.</p>
                        <p><strong>Mogi das Cruzes</strong></p>
                        <p>Em Mogi das Cruzes, o candidato a Prefeito Marcus Melo (PSDB) da coligação Mogi de Todos Nós, foi eleito com 64,34% dos votos válidos, sendo elegido logo no primeiro turno. Marcus Melo venceu dois adversários, Luiz Carlos Gondim (PSD) e Nelson Pedro Miguel (PMN).</p>
                        <p>Em Mogi das Cruzes, o candidato a Prefeito Marcus Melo (PSDB) da coligação Mogi de Todos Nós, foi eleito com 64,34% dos votos válidos, sendo elegido logo no primeiro turno. Marcus Melo venceu dois adversários, Luis Carlos Gondim (SD), que teve 32,03% de votos, e Nelson Pedro Miguel (PMN), 3,63%.</p>
