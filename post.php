                        <p><strong>Poá</strong></p>
                        <p>Gian Lopes (PR) foi eleito ao cargo de prefeito de Poá. Ele obteve 54,41% dos votos válidos e leva seu partido a assumir pela primeira vez tal posição no município.</p>
                        <p>Em segundo lugar ficou com o candidato Geraldo Oliveira (PSD) e seus 35,18% dos votos. O atual prefeito em exercício, Marcos Borges (PPS) conseguiu a terceira posição, com inexpressivos 4,15 % dos votos — inferior a quantidade de nulos, 11,26%.</p>
                        <p><strong>Taboão da Serra</strong></p>
                        <p>Em Taboão da Serra, o atual prefeito Fernando Fernandes (PSDB) continuará seu mandato. Ele foi reeleito com 53,77% dos votos, mais de 30 mil a mais que seus adversários.</p>
                        <p>Fernando Fernandes já foi prefeito da cidade de Taboão da Serra por dois mandatos, entre 1997 e 2004, retornando em 2013.</p>
                        <p>Aprígio (PDS) teve 23,63% dos votos, enquanto Buscarini (PV), que já foi prefeito da cidade entre 1982 e 1988 e voltou ao poder em 1993, teve 18% — desde a década de 90, ele não ocupou mais nenhum cargo eletivo.</p>
                        <p>Stan (PSOL) e Vitor Medeiros (PSL) tiveram cada um 2,19% e 1,96% de votos.</p>
                        <p>Quem votou em Dr. Evilásio Farias (PSB) neste domingo teve seu voto anulado. Ele, que foi prefeito da cidade entre 2004 e 2012, teve sua candidatura impugnada pelo juiz eleitoral Nelson Ricardo Cassaleiro no dia 8 de setembro.</p>
                        <p>A decisão foi baseada no artigo 50 da Resolução TSE nº 23.373/11 e Evilásio foi condenado pela prática de ato de improbidade administrativa durante seu mandato como vereador em 1996 e ficou inelegível no caso dos Congressos em 1996.</p>
